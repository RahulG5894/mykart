-- MyKart database schema
-- Runs on first Postgres startup when mounted to /docker-entrypoint-initdb.d/
-- Matches JPA entities (GenerationType.IDENTITY). Re-run requires: docker compose down -v

CREATE TABLE tbl_user (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(255),
    username        VARCHAR(255),
    email           VARCHAR(255),
    password        VARCHAR(255),
    type            VARCHAR(255),
    address         VARCHAR(255),
    CONSTRAINT tbl_user_type_check CHECK (type IN ('SELLER', 'BUYER'))
);

