CREATE TABLE item (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(255) NOT NULL,
    price           DECIMAL(18, 4) NOT NULL,
    weight          DECIMAL(10, 2),
    seller_id       BIGINT,
    CONSTRAINT fk_item_seller
        FOREIGN KEY (seller_id) REFERENCES tbl_user (id)
);

CREATE TABLE kart (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    items           JSONB,
    user_id         BIGINT NOT NULL,
    CONSTRAINT fk_kart_user
        FOREIGN KEY (user_id) REFERENCES tbl_user (id)
);

CREATE TABLE orders (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT NOT NULL,
    total_amount        DECIMAL(18, 4) NOT NULL,
    status              VARCHAR(50) NOT NULL,
    shipping_address    VARCHAR(255),
    created_on          TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_on          TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_orders_user
        FOREIGN KEY (user_id) REFERENCES tbl_user (id),
    CONSTRAINT chk_order_status
        CHECK (status IN ('CREATED', 'PLACED', 'PAID', 'SHIPPED', 'DELIVERED', 'CANCELLED'))
);

CREATE TABLE order_item (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id        BIGINT NOT NULL,
    item_id         BIGINT NOT NULL,
    quantity        INTEGER NOT NULL,
    price           DECIMAL(18, 4) NOT NULL,
    CONSTRAINT fk_order_item_order
        FOREIGN KEY (order_id) REFERENCES orders (id) ON DELETE CASCADE,
    CONSTRAINT fk_order_item_item
        FOREIGN KEY (item_id) REFERENCES item (id),
    CONSTRAINT chk_order_item_quantity
        CHECK (quantity > 0),
    CONSTRAINT uk_order_item_order_item
        UNIQUE (order_id, item_id)
);

CREATE INDEX idx_item_seller_id ON item (seller_id);
CREATE INDEX idx_kart_user_id ON kart (user_id);
CREATE INDEX idx_orders_user_id ON orders (user_id);
CREATE INDEX idx_order_item_order_id ON order_item (order_id);

